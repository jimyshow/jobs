"Nesbitt Burns" 是一家加拿大的投资银行和财富管理公司，它的名字源自于其创始人之一，名为 **William P. Nesbitt** 和公司成立时的另一个创始人 **George Burns**。
这家公司起源于 1901 年，并且后来与其他金融机构合并，成为加拿大皇家银行（RBC）的一部分。
因此，“Nesbitt Burns” 并没有直接的中文含义，而是一个由创始人名字组成的品牌。

