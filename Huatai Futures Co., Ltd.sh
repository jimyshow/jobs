
华泰期货有限公司成立于1994年3月28日，是中国首批成立的期货公司之一，目前注册资本39.39亿元，
主营业务为商品期货经纪、金融期货经纪、期货投资咨询、资产管理、基金销售。 
公司拥有上海、大连、郑州交易所会员资格和中国金融期货交易所全面结算会员资格，并首批获得中国证监会颁发的期货投资咨询业务资格、资产管理业务资格。

据华泰期货不完全统计,
第16周（4月14日至4月20日）至第19周（5月5日至5月11日），中国至美国西海岸（美西）、东海岸（美东）各有13个航次被取消，
一个月内总计取消航次达26个。这将导致中美航线的集装箱运力大幅减小，第19周的运力仅剩约23万标准箱（TEU），比第15周（4月7日至4月13日）少了14.6万TEU，下降幅度接近40%。
