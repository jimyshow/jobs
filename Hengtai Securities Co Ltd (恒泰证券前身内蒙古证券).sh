专业及学历要求
数学、统计、经济、计算机、金融、金融工程等相关专业硕士研究生及以上学历。
（二）互联网金融岗
岗位职责
1.构建全谱系的线上投顾产品，为广大投资者提供有竞争力而多样化的投顾服务；
2.协同推进产品上线；
3.定期对产品运行情况与表现进行总结评估、分析改进；
4.持续学习热门的产品设计思路和模式，并不断迭代实践。
专业及学历要求
经济、计算机、金融、金融工程等相关专业硕士研究生及以上学历。
（三）债券承做岗
岗位职责
1.负责公司债、企业债、ABS等各类债券品种的项目承做；
2.根据客户需求进行融资方案设计，组织和实施项目尽职调查，相关材料制作等工作；
3.跟踪相关监管部门政策动态，与相关机构进行协调和沟通，推进项目申报等工作；
4.负责进行项目后续管理的跟进工作。
专业及学历要求
会计、财务、金融、经济、法律等相关专业硕士研究生及以上学历。
